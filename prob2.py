# 2.	Write a Python program to do arithmetical operations addition and division.?

a = int(input("Enter your first number : "))
b = int(input("Enter your second number : "))
sum = a + b
div = a / b
div_2 = a // b
print(f"Sum of the two number is : {sum}")
print(f"Float division of the two number is : {div}")
print(f"Integer division of the two number is : {div_2}")